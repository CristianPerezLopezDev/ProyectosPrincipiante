    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        
            this.actualizarListaProveedores();
              
            cmbTipo.addItem("facturas");
            cmbTipo.addItem("albaran");
            
            String añadirCeroFechaMes=LocalDate.now().getMonthValue()<10 ? "0"+""+LocalDate.now().getMonthValue():LocalDate.now().getMonthValue()+"";
            String añadirCeroFechaDia=LocalDate.now().getDayOfMonth()<10 ? "0"+""+LocalDate.now().getDayOfMonth():LocalDate.now().getDayOfMonth()+"";
            txtAñoHa.setText(LocalDate.now().getYear()+"");
            txtMesHa.setText(añadirCeroFechaMes);
            txtDiaHa.setText(añadirCeroFechaDia);
            txtAñoDe.setText(LocalDate.now().getYear()+"");
            txtMesDe.setText(LocalDate.now().getMonthValue()<10 ? "0"+""+(LocalDate.now().getMonthValue()-1):LocalDate.now().getMonthValue()+"");
            txtDiaDe.setText(añadirCeroFechaDia);
            
    }//GEN-LAST:event_formWindowOpened

    private void btnAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirActionPerformed
       if(txtCantidad.getText().matches("[+-]?\\d*(\\.\\d+)?")&&!txtCantidad.getText().equals("")){
           
            Double valor=Double.valueOf(txtCantidad.getText());
            String nombreSeleccionado=cmbProveedoresz.getSelectedItem().toString().toUpperCase(); 
            cbd.comprobarAlbaranes(valor, nombreSeleccionado);
            cbd.registrarGasto(valor, LocalDate.now(), cmbProveedoresz.getSelectedItem().toString().toUpperCase(),cmbTipo.getSelectedItem().toString());
           
            JOptionPane.showMessageDialog(rootPane, "Registrado");        
            
       }else{
           JOptionPane.showMessageDialog(rootPane, "NO SON NUMEROS");
       }
    }//GEN-LAST:event_btnAñadirActionPerformed

