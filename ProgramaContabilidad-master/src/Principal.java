    public String formarTextoSalida(List<ResultadoSentencia> resultado){
        String salidaPantalla="";
        String añadirEspacio="";
        double total=0;
        for(int i=0;i<resultado.size();i++){
             
            añadirEspacio=resultado.get(i).getNombre();
            while(añadirEspacio.length()<25){
                añadirEspacio+="  ";
            }
            salidaPantalla+="-"+añadirEspacio+resultado.get(i).getCantidad()+"        "
                    +"        "+resultado.get(i).getFecha()+"\n";
        }
        return salidaPantalla;
    }
