    private void btnDetallesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetallesActionPerformed
        ConectorBD cb=new ConectorBD();
        List<String> listaPlatos=cb.getListaPlatos();

        int[] detallesVentas=cb.getDetallesVenta(this.getDesde(),this.getHasta());
        String resultadoPantalla="";
        for(int i=0;i<detallesVentas.length;i++){
            resultadoPantalla+=listaPlatos.get(i)+"  ------->  "+detallesVentas[i]+" und\n\n";
          
        }
        txtAreaNombre.setText(resultadoPantalla);
    }//GEN-LAST:event_btnDetallesActionPerformed

