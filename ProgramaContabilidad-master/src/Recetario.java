    private void btnVerRecetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVerRecetaActionPerformed
        List<ResultadoSentencia> resultado=cP.lanzarSentenciaSelect(
                "select ingredientes,receta,nombre from recetas where nombre='"+
                        cmbRecetas.getSelectedItem().toString()+"' and tipo ='"+
                        cmbTipoReceta.getSelectedItem().toString()+"';");
        txtAreaIngredientees.setText(resultado.get(0).getCantidad());//donde cantidad es el dato 1 por lo tanto los ingredientes
        txtAreaReceta.setText("       RECETA DE: "+cmbRecetas.getSelectedItem().toString()+"\n\n"+resultado.get(0).getNombre());
    }//GEN-LAST:event_btnVerRecetaActionPerformed

