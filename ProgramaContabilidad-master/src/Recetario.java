    private void btnAñadirRecetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirRecetaActionPerformed
        String ingredientes=txtAreaIngredientees.getText();
        String receta=txtAreaReceta.getText();
        String tipo=cmbTipoReceta.getSelectedItem().toString();
        String nombre=txtNombreReceta.getText();
        int todoOk=cP.lanzarSentencia("insert into recetas values ('"+tipo+"','"+nombre+"','"+receta+"','"+ingredientes+"');");
        
        if(todoOk==1){
            JOptionPane.showMessageDialog(rootPane,"Receta guardada");
            this.setVisible(false);
            new Recetario(new Principal(),false).setVisible(true);
        }else{
            JOptionPane.showMessageDialog(rootPane,"Revisa los datos");
        }
        
    }//GEN-LAST:event_btnAñadirRecetaActionPerformed

