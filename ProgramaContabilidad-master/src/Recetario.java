    private void btnCargarRecetasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarRecetasActionPerformed
        List<ResultadoSentencia> resultado=cP.lanzarSentenciaSelect(
                "select ingredientes,nombre,receta from recetas where tipo ='"+
                        cmbTipoReceta.getSelectedItem().toString()+"';");
        cmbRecetas.removeAllItems();
        resultado.stream()
                .forEach(e -> cmbRecetas.addItem(e.getNombre()));
       
    }//GEN-LAST:event_btnCargarRecetasActionPerformed

    private void btnAñadirTipoRecetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAñadirTipoRecetaActionPerformed
        if(!txtTipoReceta.getText().equals("")||!txtTipoReceta.getText().equals(" ")){
            cP.lanzarSentencia("Insert into tipoplato values ('"+txtTipoReceta.getText()+"');");
            cmbTipoReceta.removeAllItems();
            this.cargarTipoPlatos();
        }else{
            JOptionPane.showMessageDialog(rootPane, "rellena el tipod de plato");
        }
    }//GEN-LAST:event_btnAñadirTipoRecetaActionPerformed

